    private static readonly IAbsoluteDirectoryPath _appDir = DirectoryPath.GetAppBase();
    private static readonly IAbsoluteDirectoryPath _videosDir = _appDir.CombineDirectory("Videos");

    [TestMethod]
    [DynamicData(nameof(VideosToCheck))]
    internal async Task ValidateReading(string file, FFprobeUtils.VideoFileInfo expectedInfo)
    {
        var actualInfo = await FFprobeUtils.GetVideoFileAsync(_videosDir.CombineFile(file));
        actualInfo.FormatName.ShouldBe(expectedInfo.FormatName);
        if (actualInfo.FormatName != expectedInfo.FormatName ||
            actualInfo.Duration != expectedInfo.Duration ||
            !actualInfo.Streams.SequenceEqual(expectedInfo.Streams))
        {
            // If there's a mismatch, try to see if the mismatch still exists in a pretty-printed form for easier debugging
            ToPrettyString(actualInfo).ShouldBe(ToPrettyString(expectedInfo));

            // Otherwise, just fail the test
            throw new ShouldAssertException("Video file info does not match expected values.");
        }
    }

    private static string ToPrettyString(FFprobeUtils.VideoFileInfo info)
    {
        using StringWriter sw = new();
        using IndentedTextWriter tw = new(sw, "    ");

        tw.WriteLine("Format Name: " + info.FormatName);
        if (info.Duration is not null) tw.WriteLine("Duration: " + info.Duration);
        tw.WriteLine("Stream Count: " + info.Streams.Length);

        for (int i = 0; i < info.Streams.Length; i++)
        {
            var stream = info.Streams[i];
            tw.WriteLine("Stream " + i + ": " + stream.GetType().Name);
            tw.Indent++;

            switch (stream)
            {
                case FFprobeUtils.VideoStreamInfo videoStream:
                    tw.WriteLine("Codec Name: " + videoStream.CodecName);
                    if (videoStream.ProfileName is not null) tw.WriteLine("Profile Name: " + videoStream.ProfileName);
                    tw.WriteLine("Is Attached Pic: " + videoStream.IsAttachedPic);
                    tw.WriteLine("Is Timed Thumbnail: " + videoStream.IsTimedThumbnails);
                    if (videoStream.Width > 0) tw.WriteLine("Width: " + videoStream.Width);
                    if (videoStream.Height > 0) tw.WriteLine("Height: " + videoStream.Height);
                    if (videoStream.Duration is not null) tw.WriteLine("Duration: " + videoStream.Duration);
                    if (videoStream.FpsNum > 0) tw.WriteLine("Fps Num: " + videoStream.FpsNum);
                    if (videoStream.FpsDen > 0) tw.WriteLine("Fps Den: " + videoStream.FpsDen);
                    if (videoStream.SarNum > 0) tw.WriteLine("SAR Num: " + videoStream.SarNum);
                    if (videoStream.SarDen > 0) tw.WriteLine("SAR Den: " + videoStream.SarDen);
                    if (videoStream.PixelFormat is not null) tw.WriteLine("Pixel Format: " + videoStream.PixelFormat);
                    if (videoStream.ColorRange is not null) tw.WriteLine("Color Range: " + videoStream.ColorRange);
                    if (videoStream.ColorSpace is not null) tw.WriteLine("Color Space: " + videoStream.ColorSpace);
                    if (videoStream.ColorTransfer is not null) tw.WriteLine("Color Transfer: " + videoStream.ColorTransfer);
                    if (videoStream.ColorPrimaries is not null) tw.WriteLine("Color Primaries: " + videoStream.ColorPrimaries);
                    if (videoStream.FieldOrder is not null) tw.WriteLine("Field Order: " + videoStream.FieldOrder);
                    if (videoStream.BitsPerSample > 0) tw.WriteLine("Bits Per Sample: " + videoStream.BitsPerSample);
                    if (videoStream.AlphaMode) tw.WriteLine("Alpha Mode: " + videoStream.AlphaMode);
                    break;

                case FFprobeUtils.AudioStreamInfo audioStream:
                    tw.WriteLine("Codec Name: " + audioStream.CodecName);
                    if (audioStream.ProfileName is not null) tw.WriteLine("Profile Name: " + audioStream.ProfileName);
                    if (audioStream.Duration is not null) tw.WriteLine("Duration: " + audioStream.Duration);
                    if (audioStream.Channels > 0) tw.WriteLine("Channel: " + audioStream.Channels);
                    if (audioStream.SampleRate > 0) tw.WriteLine("Sample Rate: " + audioStream.SampleRate);
                    if (audioStream.ChannelLayout is not null) tw.WriteLine("Channel Layout: " + audioStream.ChannelLayout);
                    break;

                case FFprobeUtils.SubtitleStreamInfo subtitleStream:
                    tw.WriteLine("Codec Name: " + subtitleStream.CodecName);
                    if (subtitleStream.Language is not null) tw.WriteLine("Language: " + subtitleStream.Language);
                    if (subtitleStream.Title is not null) tw.WriteLine("Title:" + subtitleStream.Title);
                    break;

                case FFprobeUtils.UnrecognizedStreamInfo unrecognizedStream:
                    tw.WriteLine("Codec Type: " + unrecognizedStream.CodecType);
                    if (unrecognizedStream.CodecName is not null) tw.WriteLine("Codec Name: " + unrecognizedStream.CodecName);
                    if (unrecognizedStream.StreamShorthand != '\0') tw.WriteLine("Stream Shorthand: " + unrecognizedStream.StreamShorthand);
                    tw.WriteLine("Is Attached Pic: " + unrecognizedStream.IsAttachedPic);
                    tw.WriteLine("Is Timed Thumbnail: " + unrecognizedStream.IsTimedThumbnails);
                    break;

                default:
                    tw.WriteLine("Unknown stream type.");
                    break;
            }

            tw.Indent--;
        }

        tw.Flush();
        return sw.ToString();
    }

