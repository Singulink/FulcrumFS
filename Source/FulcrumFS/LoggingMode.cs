namespace FulcrumFS;

/// <summary>
/// Represents the logging mode used to write information and errors.
/// </summary>
public enum LoggingMode
{
    /// <summary>
    /// No logging is performed.
    /// </summary>
    None,

    /// <summary>
    /// Logs information and errors in a format intended for easy human reading.
    /// </summary>
    HumanReadable,
}
